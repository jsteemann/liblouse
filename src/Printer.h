namespace debugging {
  class Printer {

// -----------------------------------------------------------------------------
// --SECTION--                                        constructors / destructors
// -----------------------------------------------------------------------------

    private:

      Printer () = delete;

      ~Printer () = delete;

// -----------------------------------------------------------------------------
// --SECTION--                                                    public methods
// -----------------------------------------------------------------------------

    public:

////////////////////////////////////////////////////////////////////////////////
/// @brief emits a line
////////////////////////////////////////////////////////////////////////////////

      static void EmitLine (FILE*, char const*, ...);

////////////////////////////////////////////////////////////////////////////////
/// @brief emits an error
////////////////////////////////////////////////////////////////////////////////

      static void EmitError (FILE*, char const*, char const*, ...); 

////////////////////////////////////////////////////////////////////////////////
/// @brief whether or not we should use color printing
////////////////////////////////////////////////////////////////////////////////

      static bool UseColors (FILE*);

  };
}

#endif
